var color = "white";
switch (color){
    case 'blue':
        console.log('It is blue');
        break;
    case 'red':
        console.log('It is red');
        break;
    case 'green':
        console.log('It is green');
        break;
    case 'pink':
        console.log('It is pink');
        break;
    case 'gray':
        console.log('It is gray');
        break;
    case 'white':
        console.log('It is white');
        break;
        default:
            console.log(' It is nothing');

}